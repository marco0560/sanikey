def run_validate_config(args: argparse.Namespace) -> int:
    """Validate accounts configuration and privacy constraints.

    Parameters
    ----------
    args : argparse.Namespace
        Parsed command arguments.

    Returns
    -------
    int
        Process exit status.
    """

    try:
        config = load_accounts(_config_path(args))
        validate_privacy(config, repo_root=args.repo_root)
        for person in config.enabled_people():
            load_curated_metadata(person.metadata_directory)
    except SaniKeyError as exc:
        print(f"ERRORE: {exc}")
        return 1
    enabled = len(config.enabled_people())
    total = len(config.people)
    print(f"configurazione={_repo_relative_path(config.path)}")
    print(f"pazienti={total}")
    print(f"abilitati={enabled}")
    print("stato=ok")
    return 0


def run_list_patients(args: argparse.Namespace) -> int:
    """List configured patient identifiers.

    Parameters
    ----------
    args : argparse.Namespace
        Parsed command arguments.

    Returns
    -------
    int
        Process exit status.
    """

    try:
        config = load_accounts(_config_path(args))
    except SaniKeyError as exc:
        print(f"ERRORE: {exc}")
        return 1
    selected = sorted(
        config.people if args.all else config.enabled_people(),
        key=lambda person: (
            not person.enabled,
            person.id.casefold(),
        ),
    )
    id_width = max((len(person.id) for person in selected), default=0)
    state_width = len("disabilitato") if args.all else len("abilitato")
    for person in selected:
        state = "abilitato" if person.enabled else "disabilitato"
        print(
            f"{person.id:<{id_width}}   {state:<{state_width}}   {person.display_name}"
        )
    return 0


def run_scan_documents(args: argparse.Namespace) -> int:
    """Scan configured source documents and optionally print inventory rows.

    Parameters
    ----------
    args : argparse.Namespace
        Parsed command arguments.

    Returns
    -------
    int
        Process exit status.
    """

    if args.output is None and args.format is not None:
        print("ERRORE: --format e' valido solo con --output")
        return 1
    try:
        config = load_accounts(_config_path(args))
        selected_people = _selected_people(config, args.patient)
        for person in selected_people:
            load_curated_metadata(person.metadata_directory)
    except SaniKeyError as exc:
        print(f"ERRORE: {exc}")
        return 1
    output_rows: list[tuple[PersonConfig, DocumentRecord]] = []
    progress = _progress_from_args(args)
    for person in selected_people:
        inspection = inspect_patient_documents(
            person,
            preflight=args.preflight,
            stage_containers=not args.no_stage_containers,
            progress=progress,
        )
        warning_messages = (
            *inspection.warning_messages,
            *inspection.preflight_warning_messages,
        )
        summary = (
            f"paziente={person.id} files={len(inspection.inventory)} "
            f"documenti={len(inspection.documents)} "
            f"duplicati={len(inspection.duplicates)} avvisi={len(warning_messages)} "
            f"esclusi={len(inspection.excluded_files)}"
        )
        if inspection.container_staging is not None:
            staged_container_ids = {
                member.container_id for member in inspection.container_staging.members
            }
            summary = (
                f"{summary} archivi_preparati={len(staged_container_ids)} "
                f"membri_in_archivi={len(inspection.container_staging.members)} "
                f"documenti_derivati={len(inspection.container_staging.documents)}"
            )
        print(summary)
        for warning in warning_messages:
            print(f"AVVISO: {warning}")
        if args.verbose:
            print(_format_scan_verbose(person, inspection.documents))
        output_rows.extend((person, document) for document in inspection.documents)
    if args.output is not None:
        try:
            _write_scan_output(
                args.output,
                output_rows,
                output_format=args.format or "text",
            )
        except OSError as exc:
            print(
                "ERRORE: impossibile scrivere l'output della scansione "
                f"{_repo_relative_path(args.output)}: {_repo_relative_text(str(exc))}"
            )
            return 1
    return 0


def run_document_integrity(args: argparse.Namespace) -> int:
    """Create or verify source document integrity snapshots.

    Parameters
    ----------
    args : argparse.Namespace
        Parsed command arguments.

    Returns
    -------
    int
        Process exit status.
    """

    try:
        config = load_accounts(_config_path(args))
        selected_people = _selected_people(config, args.patient)
    except SaniKeyError as exc:
        print(f"ERRORE: {exc}")
        return 1
    status = 0
    for person in selected_people:
        try:
            if args.action in {"before", "after"}:
                result = write_source_snapshot(
                    person,
                    label=args.action,
                    output_dir=args.output_dir,
                )
            else:
                result = check_source_snapshots(person, output_dir=args.output_dir)
        except OSError as exc:
            print(f"ERRORE: paziente={person.id} {exc}")
            status = 1
            continue
        print(
            f"paziente={result.patient_id} azione={args.action} "
            f"stato={result.status} "
            f"sha256={_repo_relative_path(result.sha256_path)} "
            f"mtime={_repo_relative_path(result.mtime_path)}"
        )
        if result.status != "ok" and args.action == "check":
            status = 1
    return status


def _format_scan_verbose(
    person: PersonConfig,
    documents: tuple[DocumentRecord, ...],
) -> str:
    """Render a readable scan inventory table for stdout.

    Parameters
    ----------
    person : PersonConfig
        Patient configuration.
    documents : tuple[DocumentRecord, ...]
        Deduplicated document records.

    Returns
    -------
    str
        Human-readable table.
    """

    if not documents:
        return f"paziente={person.id} documenti_acquisiti=0"
    rows = [
        (
            "paziente",
            "tipo",
            "data",
            "categoria",
            "sha256",
            "file",
        ),
        *(
            (
                document.patient_id,
                document.kind,
                _format_display_date(document.date),
                document.category,
                document.sha256[:12],
                _source_relative_path(person, document),
            )
            for document in documents
        ),
    ]
    widths = tuple(
        max(len(row[index]) for row in rows) for index in range(len(rows[0]))
    )
    rendered = [f"paziente={person.id} documenti_acquisiti={len(documents)}"]
    for row_index, row in enumerate(rows):
        rendered.append(_format_table_row(row, widths))
        if row_index == 0:
            rendered.append(_format_scan_separator(row, widths))
    return "\n".join(rendered)


def _format_table_row(row: tuple[str, ...], widths: tuple[int, ...]) -> str:
    """Render one table row without padding the final column.

    Parameters
    ----------
    row : tuple[str, ...]
        Table row values.
    widths : tuple[int, ...]
        Column widths.

    Returns
    -------
    str
        Rendered row.
    """

    padded = [value.ljust(widths[index]) for index, value in enumerate(row[:-1])]
    return "  ".join((*padded, row[-1])).rstrip()


def _format_scan_separator(row: tuple[str, ...], widths: tuple[int, ...]) -> str:
    """Render the scan table separator.

    Parameters
    ----------
    row : tuple[str, ...]
        Header row.
    widths : tuple[int, ...]
        Column widths.

    Returns
    -------
    str
        Separator row.
    """

    separator = tuple(
        "-" * (4 if index == len(row) - 1 else min(widths[index], 20))
        for index in range(len(row))
    )
    return _format_table_row(separator, widths)


def _write_scan_output(
    output_path: Path,
    rows: list[tuple[PersonConfig, DocumentRecord]],
    *,
    output_format: str,
) -> None:
    """Write scan inventory rows to a user-selected file.

    Parameters
    ----------
    output_path : pathlib.Path
        Destination file path.
    rows : list[tuple[PersonConfig, DocumentRecord]]
        Patient and document rows.
    output_format : str
        Output format, either ``text`` or ``csv``.

    Returns
    -------
    None
    """

    output_path.parent.mkdir(parents=True, exist_ok=True)
    if output_format == "csv":
        _write_scan_csv(output_path, rows)
        return
    _write_scan_text(output_path, rows)


def _write_scan_csv(
    output_path: Path,
    rows: list[tuple[PersonConfig, DocumentRecord]],
) -> None:
    """Write scan inventory rows as CSV.

    Parameters
    ----------
    output_path : pathlib.Path
        Destination file path.
    rows : list[tuple[PersonConfig, DocumentRecord]]
        Patient and document rows.

    Returns
    -------
    None
    """

    with output_path.open("w", encoding="utf-8", newline="") as handle:
        writer = csv.writer(handle)
        writer.writerow(
            ("paziente", "tipo", "categoria", "data", "titolo", "sha256", "path")
        )
        for _person, document in rows:
            writer.writerow(_scan_legacy_row(document))


def _write_scan_text(
    output_path: Path,
    rows: list[tuple[PersonConfig, DocumentRecord]],
) -> None:
    """Write scan inventory rows in legacy tab-separated text format.

    Parameters
    ----------
    output_path : pathlib.Path
        Destination file path.
    rows : list[tuple[PersonConfig, DocumentRecord]]
        Patient and document rows.

    Returns
    -------
    None
    """

    lines = ["\t".join(_scan_legacy_row(document)) for _person, document in rows]
    output_path.write_text("\n".join(lines) + ("\n" if lines else ""), encoding="utf-8")


