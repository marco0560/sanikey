def _format_display_date(value: str | None) -> str:
    """Format an ISO date for display.

    Parameters
    ----------
    value : str | None
        ISO date or missing value.

    Returns
    -------
    str
        Italian display date or empty string.
    """

    if value is None:
        return ""
    match = value.split("-")
    if len(match) == 3 and all(part.isdigit() for part in match):
        return f"{match[2]}/{match[1]}/{match[0]}"
    return value


def _source_relative_path(person: PersonConfig, document: DocumentRecord) -> str:
    """Return a document path relative to the source root when possible.

    Parameters
    ----------
    person : PersonConfig
        Patient configuration.
    document : DocumentRecord
        Document record.

    Returns
    -------
    str
        Relative path or absolute path fallback.
    """

    try:
        return document.path.relative_to(person.source_documents).as_posix()
    except ValueError:
        return str(document.path)


def _repo_relative_path(path: Path, *, repo_root: Path | None = None) -> str:
    """Return a path relative to the repository root when possible.

    Parameters
    ----------
    path : pathlib.Path
        Path to render.
    repo_root : pathlib.Path | None, optional
        Repository root. When omitted, the current working directory is used.

    Returns
    -------
    str
        Repository-relative path when ``path`` is inside ``repo_root``,
        otherwise the original path string.
    """

    selected_root = Path.cwd() if repo_root is None else repo_root
    try:
        return (
            path.resolve(strict=False)
            .relative_to(selected_root.resolve(strict=False))
            .as_posix()
        )
    except ValueError:
        return str(path)


def _repo_relative_text(value: str, *, repo_root: Path | None = None) -> str:
    """Abbreviate repository paths embedded in a diagnostic message.

    Parameters
    ----------
    value : str
        Diagnostic text that can contain one or more absolute paths.
    repo_root : pathlib.Path | None, optional
        Repository root. When omitted, the current working directory is used.

    Returns
    -------
    str
        Diagnostic text with paths below the repository rendered relatively.
    """

    selected_root = Path.cwd() if repo_root is None else repo_root
    root_text = f"{selected_root.resolve(strict=False)}/"
    return value.replace(root_text, "")


def _format_extract_text_result(
    person: PersonConfig,
    document: DocumentRecord,
    extracted: ExtractedText,
    *,
    repo_root: Path | None = None,
    verbose: bool = False,
) -> str:
    """Format one text extraction result for human-readable CLI output.

    Parameters
    ----------
    person : PersonConfig
        Patient configuration.
    document : DocumentRecord
        Source document.
    extracted : sanikey.documents.ExtractedText
        Extracted text payload.
    repo_root : pathlib.Path | None, optional
        Repository root used to abbreviate paths.
    verbose : bool, optional
        Whether to include the short SHA-256 content fingerprint.

    Returns
    -------
    str
        One aligned output row with patient id, page count, warning count
        and source path. Verbose output also includes the short SHA-256 content
        fingerprint.
    """

    fingerprint = f"sha256={document.sha256[:12]} " if verbose else ""
    pages = document_page_count(document)
    rendered_pages = "?" if pages is None else str(pages)
    return (
        f"{person.id:<12} "
        f"{fingerprint}"
        f"pagine={rendered_pages:>5} "
        f"avvisi={len(extracted.warnings):>3} "
        f"{_repo_relative_path(document.path, repo_root=repo_root)}"
    )


