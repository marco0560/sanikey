class ItalianArgumentParser(argparse.ArgumentParser):
    """Argument parser with Italian diagnostic text.

    Parameters
    ----------
    *args : object
        Positional arguments forwarded to ``argparse.ArgumentParser``.
    **kwargs : object
        Keyword arguments forwarded to ``argparse.ArgumentParser``.
    """

    def error(self, message: str) -> Never:
        """Print a localized parser error and exit.

        Parameters
        ----------
        message : str
            Error message generated by ``argparse``.

        Returns
        -------
        Never

        Raises
        ------
        SystemExit
            Always raised with exit status ``2``.
        """

        self.print_usage()
        self.exit(2, f"{self.prog}: errore: {_translate_argparse_error(message)}\n")


def _translate_argparse_error(message: str) -> str:
    """Translate common ``argparse`` diagnostics to Italian.

    Parameters
    ----------
    message : str
        Diagnostic message generated by ``argparse``.

    Returns
    -------
    str
        Localized diagnostic message.
    """

    replacements = (
        ("unrecognized arguments:", "argomenti non riconosciuti:"),
        ("the following arguments are required:", "argomenti obbligatori mancanti:"),
        ("invalid choice:", "scelta non valida:"),
        ("expected one argument", "atteso un argomento"),
        ("expected at least one argument", "atteso almeno un argomento"),
    )
    translated = message
    for source, target in replacements:
        translated = translated.replace(source, target)
    return translated


